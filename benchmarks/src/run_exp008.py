BUNDLE_METHODS = {"cdxf", "tar_gz", "mega_json", "pickle"}

_FORMAT_TO_SOURCE = {
    "json": SourceFormat.JSON,
    "yaml": SourceFormat.YAML,
    "toml": SourceFormat.TOML,
    "xml": SourceFormat.XML,
    "markdown": SourceFormat.UNSPECIFIED,
    "text": SourceFormat.UNSPECIFIED,
}

_FROM_BRIDGE = {
    "json": from_json,
    "yaml": from_yaml,
    "toml": from_toml,
}

_TO_BRIDGE = {
    "json": lambda stream: to_json(stream, indent=2),
    "yaml": to_yaml,
    "toml": to_toml,
}


