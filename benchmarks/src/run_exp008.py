def _find_cdxf_doc_by_filename(stream: Stream, filename: str):
    """Find a Document in a CDXF stream by its preamble filename comment."""
    for i, doc in enumerate(stream.documents):
        for node in doc.preamble:
            if isinstance(node, Comment):
                if node.text.strip().startswith("cdxf-bundle-filename:"):
                    doc_filename = node.text.strip().split(":", 1)[1].strip()
                    if doc_filename == filename:
                        return i, doc
    return None, None


def extract_component(bundle: bytes, method: str,
                      filename: str) -> dict:
    """Extract a single component from a bundle.

    Returns dict with: success, text, time_ns, error.
    """
    result = {"success": False, "text": "", "time_ns": 0, "error": None}

    try:
        t_start = time.perf_counter_ns()

        if method == "cdxf":
            stream = decode(bundle)
            idx, doc = _find_cdxf_doc_by_filename(stream, filename)
            if doc is None:
                result["error"] = f"Component '{filename}' not found"
                return result
            # Emit as single-doc stream via appropriate bridge
            single = Stream(documents=[doc])
            fmt_hint = doc.source_format_hint
            if fmt_hint == SourceFormat.JSON:
                result["text"] = to_json(single, indent=2)
            elif fmt_hint == SourceFormat.YAML:
                result["text"] = to_yaml(single)
            elif fmt_hint == SourceFormat.TOML:
                result["text"] = to_toml(single)
            else:
                # Raw scalar (markdown, text, etc.)
                if isinstance(doc.root, Scalar):
                    result["text"] = str(doc.root.value)
                else:
                    result["text"] = to_json(single, indent=2)

        elif method == "tar_gz":
            buf = io.BytesIO(bundle)
            with tarfile.open(fileobj=buf, mode="r:gz") as tar:
                member = tar.getmember(filename)
                f = tar.extractfile(member)
                result["text"] = f.read().decode("utf-8")

        elif method == "mega_json":
            parsed = json.loads(bundle.decode("utf-8"))
            if filename not in parsed:
                result["error"] = f"Component '{filename}' not found"
                return result
            val = parsed[filename]
            if isinstance(val, str):
                result["text"] = val
            else:
                result["text"] = json.dumps(val, indent=2)

        elif method == "pickle":
            parsed = pickle.loads(bundle)
            if filename not in parsed:
                result["error"] = f"Component '{filename}' not found"
                return result
            val = parsed[filename]
            if isinstance(val, str):
                result["text"] = val
            else:
                result["text"] = json.dumps(val, indent=2)

        else:
            result["error"] = f"Unknown method: {method}"
            return result

        t_end = time.perf_counter_ns()
        result["success"] = True
        result["time_ns"] = t_end - t_start

    except Exception as e:
        result["error"] = str(e)

    return result


