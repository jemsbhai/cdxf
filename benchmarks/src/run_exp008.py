def bundle_cdxf_multidoc(components: list[dict]) -> bytes:
    """Bundle components as a CDXF multi-document stream.

    Each component becomes a Document in the Stream. The filename
    is stored as a Comment in the document's preamble. Non-bridgeable
    formats (markdown, text) are stored as raw Scalar strings.
    """
    documents = []

    for comp in components:
        filename = comp["filename"]
        fmt = comp["format"]
        text = comp["text"]

        # Parse via bridge if supported, otherwise wrap as raw string
        if fmt in _FROM_BRIDGE:
            bridge = _FROM_BRIDGE[fmt]
            stream = bridge(text)
            doc = stream.documents[0]
        else:
            # Store as raw string scalar
            root = Scalar(scalar_type=ScalarType.STRING, value=text)
            doc = Document(
                root=root,
                source_format_hint=_FORMAT_TO_SOURCE.get(fmt, SourceFormat.UNSPECIFIED),
            )

        # Tag with filename via preamble comment
        doc.preamble.insert(0, Comment(text=f"cdxf-bundle-filename: {filename}"))
        doc.source_format_hint = _FORMAT_TO_SOURCE.get(fmt, SourceFormat.UNSPECIFIED)
        documents.append(doc)

    stream = Stream(documents=documents)
    return encode(stream)


def bundle_tar_gz(components: list[dict]) -> bytes:
    """Bundle components as a tar.gz archive."""
    buf = io.BytesIO()
    with tarfile.open(fileobj=buf, mode="w:gz") as tar:
        for comp in components:
            data = comp["text"].encode("utf-8")
            info = tarfile.TarInfo(name=comp["filename"])
            info.size = len(data)
            tar.addfile(info, io.BytesIO(data))
    return buf.getvalue()


def bundle_mega_json(components: list[dict]) -> bytes:
    """Bundle components as a single merged JSON object.

    Non-JSON components are stored as raw strings under their filename.
    JSON components are parsed and inlined. All comments/metadata lost.
    """
    merged = {}
    for comp in components:
        if comp["format"] == "json":
            try:
                merged[comp["filename"]] = json.loads(comp["text"])
            except json.JSONDecodeError:
                merged[comp["filename"]] = comp["text"]
        else:
            merged[comp["filename"]] = comp["text"]

    return json.dumps(merged, indent=2).encode("utf-8")


def bundle_pickle(components: list[dict]) -> bytes:
    """Bundle components as a pickled dict."""
    data = {}
    for comp in components:
        if comp["format"] == "json":
            try:
                data[comp["filename"]] = json.loads(comp["text"])
            except json.JSONDecodeError:
                data[comp["filename"]] = comp["text"]
        else:
            data[comp["filename"]] = comp["text"]
    return pickle.dumps(data)


