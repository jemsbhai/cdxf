def _count_metadata_in_text(text: str, fmt: str) -> int:
    """Count metadata constructs in text (comments, anchors, etc.)."""
    count = 0
    if fmt == "yaml":
        for line in text.splitlines():
            if line.strip().startswith("#"):
                count += 1
        count += len(re.findall(r'&\w+', text))
    elif fmt == "toml":
        for line in text.splitlines():
            if line.strip().startswith("#"):
                count += 1
    elif fmt == "markdown":
        # YAML frontmatter counts
        if text.startswith("---"):
            count += 1  # has frontmatter
    return count


def measure_bundle(adapter: dict) -> dict:
    """Run all bundling methods on an adapter and compare."""
    components = adapter["components"]
    total_input = sum(len(c["text"].encode("utf-8")) for c in components)

    # Count source metadata
    total_metadata = sum(
        _count_metadata_in_text(c["text"], c["format"])
        for c in components
    )

    result = {
        "name": adapter["name"],
        "n_components": len(components),
        "total_input_size": total_input,
        "total_source_metadata": total_metadata,
    }

    # Bundle with each method
    bundlers = {
        "cdxf": bundle_cdxf_multidoc,
        "tar_gz": bundle_tar_gz,
        "mega_json": bundle_mega_json,
        "pickle": bundle_pickle,
    }

    for method, bundler in bundlers.items():
        t_start = time.perf_counter_ns()
        try:
            bundle = bundler(components)
            t_end = time.perf_counter_ns()

            method_result = {
                "success": True,
                "bundle_size": len(bundle),
                "size_vs_sum": len(bundle) / total_input if total_input > 0 else 0,
                "bundle_time_ns": t_end - t_start,
                "error": None,
            }

            # Test extraction of adapter_config.json
            ext = extract_component(bundle, method, "adapter_config.json")
            method_result["extract_success"] = ext["success"]
            method_result["extract_time_ns"] = ext["time_ns"]

            # CDXF-specific: check round-trip fidelity and metadata
            if method == "cdxf":
                fidelity_pass = 0
                fidelity_total = 0
                for comp in components:
                    if comp["format"] in _FROM_BRIDGE:
                        fidelity_total += 1
                        extracted = extract_component(
                            bundle, "cdxf", comp["filename"]
                        )
                        if extracted["success"] and len(extracted["text"]) > 0:
                            fidelity_pass += 1

                method_result["round_trip_fidelity"] = (
                    fidelity_pass / fidelity_total
                    if fidelity_total > 0 else 1.0
                )

                # Count metadata preserved in CDXF
                decoded = decode(bundle)
                cdxf_comments = 0
                for doc in decoded.documents:
                    # Don't count the filename comment we injected
                    for node in doc.preamble:
                        if isinstance(node, Comment):
                            if not node.text.strip().startswith(
                                "cdxf-bundle-filename:"
                            ):
                                cdxf_comments += 1
                    # Walk root for comments
                    cdxf_comments += _walk_count_comments(doc.root)
                method_result["metadata_preserved"] = cdxf_comments

        except Exception as e:
            t_end = time.perf_counter_ns()
            method_result = {
                "success": False,
                "bundle_size": 0,
                "size_vs_sum": 0,
                "bundle_time_ns": t_end - t_start,
                "error": str(e),
            }

        result[method] = method_result

    return result


def _walk_count_comments(node) -> int:
    """Count Comment nodes in a CDXF tree."""
    count = 0
    if isinstance(node, Comment):
        return 1
    elif isinstance(node, Map):
        for entry in node.entries:
            if isinstance(entry, Comment):
                count += 1
            elif isinstance(entry, tuple) and len(entry) == 2:
                count += _walk_count_comments(entry[0])
                count += _walk_count_comments(entry[1])
    elif isinstance(node, Sequence):
        for item in node.items:
            count += _walk_count_comments(item)
    elif isinstance(node, Document):
        if node.root is not None:
            count += _walk_count_comments(node.root)
    return count


