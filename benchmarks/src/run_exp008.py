def main():
    """Run EXP-008: LoRA Adapter Registry Metadata Bundle."""
    import csv

    print("=" * 70)
    print("EXP-008: LoRA Adapter Registry Metadata Bundle")
    print(f"Date: {time.strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 70)

    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    corpus = build_adapter_corpus()
    print(f"\nAdapter corpus: {len(corpus)} adapters")

    results = []
    for i, adapter in enumerate(corpus, 1):
        print(f"\n--- Adapter {i}/{len(corpus)}: {adapter['name']} ---")
        print(f"    Components: {len(adapter['components'])}, "
              f"base: {adapter['base_model']}")

        result = measure_bundle(adapter)
        results.append(result)

        for method in BUNDLE_METHODS:
            mr = result[method]
            if mr["success"]:
                size = mr["bundle_size"]
                ratio = mr["size_vs_sum"]
                print(f"    {method:10s}: {size:6d}B "
                      f"(ratio={ratio:.3f})", end="")
                if method == "cdxf":
                    print(f"  fidelity={mr.get('round_trip_fidelity', 'N/A')}"
                          f"  metadata={mr.get('metadata_preserved', 0)}", end="")
                print()
            else:
                print(f"    {method:10s}: FAIL {mr['error']}")

    # Write CSV
    csv_path = RESULTS_DIR / "bundle_results.csv"
    fieldnames = [
        "name", "n_components", "total_input_size", "total_source_metadata",
        "cdxf_size", "cdxf_ratio", "cdxf_fidelity", "cdxf_metadata",
        "tar_gz_size", "tar_gz_ratio",
        "mega_json_size", "mega_json_ratio",
        "pickle_size", "pickle_ratio",
    ]
    with open(csv_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for r in results:
            row = {
                "name": r["name"],
                "n_components": r["n_components"],
                "total_input_size": r["total_input_size"],
                "total_source_metadata": r["total_source_metadata"],
            }
            for method in ["cdxf", "tar_gz", "mega_json", "pickle"]:
                mr = r[method]
                row[f"{method}_size"] = mr["bundle_size"] if mr["success"] else ""
                row[f"{method}_ratio"] = (
                    f"{mr['size_vs_sum']:.4f}" if mr["success"] else ""
                )
            row["cdxf_fidelity"] = r["cdxf"].get("round_trip_fidelity", "")
            row["cdxf_metadata"] = r["cdxf"].get("metadata_preserved", "")
            writer.writerow(row)
    print(f"\nPer-adapter results: {csv_path}")

    # Cross-format emission test
    print("\n" + "=" * 70)
    print("CROSS-FORMAT EMISSION (CDXF-only capability)")
    print("=" * 70)

    cross_results = []
    adapter = corpus[0]  # Use first adapter
    bundle = bundle_cdxf_multidoc(adapter["components"])
    for comp in adapter["components"]:
        if comp["format"] in _FROM_BRIDGE:
            for target in ["json", "yaml", "toml"]:
                if target == comp["format"]:
                    continue
                r = cross_format_emit(bundle, comp["filename"], target)
                status = "OK" if r["success"] else f"FAIL: {r['error']}"
                print(f"    {comp['filename']} ({comp['format']}) -> "
                      f"{target}: {status}")
                cross_results.append({
                    "filename": comp["filename"],
                    "original": comp["format"],
                    "target": target,
                    "success": r["success"],
                    "error": r.get("error"),
                })

    cross_path = RESULTS_DIR / "cross_format_emit.json"
    with open(cross_path, "w") as f:
        json.dump(cross_results, f, indent=2)

    # Summary
    print("\n" + "=" * 70)
    print("HEADLINE FINDINGS")
    print("=" * 70)

    # Average size ratios
    for method in ["cdxf", "tar_gz", "mega_json", "pickle"]:
        ratios = [r[method]["size_vs_sum"] for r in results
                  if r[method]["success"]]
        if ratios:
            import statistics
            med = statistics.median(ratios)
            print(f"  {method:10s} median size ratio: {med:.3f}")

    # CDXF fidelity
    fidelities = [r["cdxf"].get("round_trip_fidelity", 0) for r in results
                  if r["cdxf"]["success"]]
    if fidelities:
        print(f"\n  CDXF round-trip fidelity: "
              f"{sum(fidelities)/len(fidelities):.1%}")

    # CDXF metadata
    total_src = sum(r["total_source_metadata"] for r in results)
    total_preserved = sum(r["cdxf"].get("metadata_preserved", 0)
                          for r in results if r["cdxf"]["success"])
    print(f"  CDXF metadata preserved: {total_preserved} "
          f"(source had {total_src} constructs)")

    # Cross-format
    n_cross = sum(1 for c in cross_results if c["success"])
    print(f"  Cross-format emissions: {n_cross}/{len(cross_results)} succeeded")

    # Write summary
    summary_path = RESULTS_DIR / "summary.json"
    summary = {
        "n_adapters": len(corpus),
        "bundle_methods": list(BUNDLE_METHODS),
        "size_ratios": {
            method: {
                "median": statistics.median(
                    [r[method]["size_vs_sum"] for r in results
                     if r[method]["success"]]
                ),
            }
            for method in BUNDLE_METHODS
        },
        "cdxf_fidelity": sum(fidelities) / len(fidelities) if fidelities else 0,
        "cdxf_metadata_preserved": total_preserved,
        "source_metadata_total": total_src,
        "cross_format_success": n_cross,
        "cross_format_total": len(cross_results),
    }
    with open(summary_path, "w") as f:
        json.dump(summary, f, indent=2)
    print(f"\nFull summary: {summary_path}")
    print("\nDone.")


