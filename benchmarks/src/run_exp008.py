# Cross-format emission (CDXF-only capability)
# ---------------------------------------------------------------------------

def cross_format_emit(cdxf_bundle: bytes, filename: str,
                           target_format: str) -> dict:
    """Extract a component from CDXF and emit in a different format.

    This is a CDXF-unique capability: the original format metadata is
    preserved, but the data can be emitted in any supported format.

    Returns dict with: success, text, original_format, target_format, error.
    """
    result = {
        "success": False,
        "text": "",
        "original_format": None,
        "target_format": target_format,
        "error": None,
    }

    try:
        stream = decode(cdxf_bundle)
        idx, doc = _find_cdxf_doc_by_filename(stream, filename)
        if doc is None:
            result["error"] = f"Component '{filename}' not found"
            return result

        result["original_format"] = doc.source_format_hint.name.lower()

        # Emit via target bridge
        single = Stream(documents=[doc])
        if target_format not in _TO_BRIDGE:
            result["error"] = f"No bridge for target format: {target_format}"
            return result

        to_bridge = _TO_BRIDGE[target_format]
        result["text"] = to_bridge(single)
        result["success"] = True

    except Exception as e:
        result["error"] = str(e)

    return result


