def _make_adapter_config(base_model: str, r: int = 16,
                         lora_alpha: int = 32,
                         target_modules: list[str] | None = None,
                         task_type: str = "CAUSAL_LM") -> str:
    """Generate a realistic adapter_config.json."""
    if target_modules is None:
        target_modules = ["q_proj", "v_proj"]
    return json.dumps({
        "base_model_name_or_path": base_model,
        "bias": "none",
        "fan_in_fan_out": False,
        "inference_mode": True,
        "init_lora_weights": True,
        "lora_alpha": lora_alpha,
        "lora_dropout": 0.05,
        "modules_to_save": None,
        "peft_type": "LORA",
        "r": r,
        "target_modules": target_modules,
        "task_type": task_type,
    }, indent=2)


